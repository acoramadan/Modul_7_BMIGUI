    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
 
      double weight = Double.parseDouble(Weight.getText());
      double height = Double.parseDouble(Height.getText());
      BMI.setText(String.format("%.9f", bmi(weight,height)));
      double Bmi = bmi(Double.parseDouble(Weight.getText()),Double.parseDouble(Height.getText()));
        if(Bmi < 18.5){
            Kategori.setText("Berat Badan Kurang");
        }
        else if(Bmi >= 18.5 && Bmi <= 24.9){
            Kategori.setText("Normal");
        }
        else if(Bmi >= 25 && Bmi <= 29.9 ){
            Kategori.setText("Kelebihan berat badan");
        }
        else if(Bmi >= 30 && Bmi <= 34.9){
            Kategori.setText("Obesitas Kelas 1");
        }
        else if(Bmi >=35 && Bmi <=39.9){
            Kategori.setText("Obesitas kelas 2");
        }
        else{
            Kategori.setText("Obesitas kelas 3");
        }   
      
    }//GEN-LAST:event_jButton1ActionPerformed

